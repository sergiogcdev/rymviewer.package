package view;

/**
 *
 * @author sergio
 */
public class Option {
    
    private int id;
    private String desc;
    
    public Option() {
        //Empty
    }

    public Option(int id, String desc) {
        this.id = id;
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
    
    
    
}
